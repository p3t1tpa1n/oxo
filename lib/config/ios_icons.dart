  // Couleurs par défaut pour les icônes contextuelles
  static final Map<String, Color> contextColors = {
    'primary': const Color(0xFF007AFF),    // Bleu système
    'success': const Color(0xFF34C759),    // Vert système
    'warning': const Color(0xFFFF9500),    // Orange système
    'danger': const Color(0xFFFF3B30),     // Rouge système
    'info': const Color(0xFF5AC8FA),       // Bleu clair système
    'secondary': const Color(0xFF8E8E93),  // Gris système
  };

  /// Méthode utilitaire pour obtenir une icône avec sa couleur contextuelle
  static Widget contextIcon(IconData icon, String context, {double size = 24}) {
    return Icon(
      icon,
      color: contextColors[context] ?? contextColors['secondary'],
      size: size,
    );
  }

  /// Méthode pour créer un conteneur d'icône avec arrière-plan coloré (style iOS)
  static Widget iconContainer(
    IconData icon,
    String context, {
    double size = 36,
    double iconSize = 20,
  }) {
    final color = contextColors[context] ?? contextColors['primary']!;
    
    return Container(
      width: size,
      height: size,
      decoration: BoxDecoration(
        color: color,
        borderRadius: BorderRadius.circular(8),
      ),
      child: Icon(
        icon,
        color: const Color(0xFFFFFFFF),
        size: iconSize,
      ),
    );
  }
